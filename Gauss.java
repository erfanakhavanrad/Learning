public class Gauss
{
    public static void main(String [] args)
    {
        int n,sum;
        n=100;
        sum=(n+1)*n/2;
        System.out.print("The sum of integers from 1 to ");
        System.out.print(n);
        System.out.print("=");
        System.out.println(sum);
        n=1000;
        sum=(n+1)*n/2;
        System.out.print("The sum of integers from 1 to ");
        System.out.print(n);
        System.out.print("=");
        System.out.println(sum);
    }
}